                <li>Les Applications Android, Juin 2010 : <a href="http://www.lesapplicationsandroid.fr/2010/06/yaam-un-market-alternatif-prometteur/">http://www.lesapplicationsandroid.fr/2010/06/yaam-un-market-alternatif-prometteur/</a></li>
