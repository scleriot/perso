<h2>YAAM : Yet Another Android Market</h2>
<div class="row">
    <div class="col-md-12">
        <p>Lancé en mars 2010, YAAM était un projet ambitieux de marché d'application Android alternatif à celui de Google, et Open-Source qui plus est.</p>
        <p>Le but était de combler les nombreux manques de l'Android Market de l'époque, YAAM proposait en effet entre autre les fonctionnalités suivantes :
            <ul>
                <li>Paiement via Paypal</li>
                <li>Possibilité aux développeurs de répondre aux commentaires des utilisateurs</li>
                <li>Formatage des descriptions des applications en HTML</li>
                <li>Possibilité de se faire rembourser l'application dans les 24h suivant son achat</li>
                <li>Ré-installation et mise à jour en un clic de ses applications (pratique lors d'une remise à zéro du téléphone)</li>
            </ul>
        </p>
        <p>Acheté en 2011 par l'entreprise XDA-Developers, YAAM est peu à peu tombé à l'abandon, pour être finalement discontinué dès 2013.</p>
        <p>Lors de sa vente, YAAM totalisait une 100aine d'applications, et plus de 25 000 utilisateurs enregistrés en un peu plus d'un an !</p>
        <p>Couverture presse 2010-2011 :
